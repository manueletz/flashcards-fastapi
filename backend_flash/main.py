@app.get("/cards", response_model=List[Flashcard])
def list_cards():
    """
    Lista todas las flashcards del esquema FLASH.
    """
    conn = get_connection()
    cur = conn.cursor()
    try:
        cur.execute(
            """
            SELECT id, english, spanish, created_at
            FROM FLASH.FLASHCARDS
            ORDER BY id
            """
        )
        rows = cur.fetchall()

        cards = [
            Flashcard(
                id=row[0],
                english=row[1],
                spanish=row[2],
                created_at=row[3]
            )
            for row in rows
        ]
        return cards

    except Exception as e:
        print("Error en GET /cards:", e)
        raise HTTPException(status_code=500, detail=str(e))
    finally:
        cur.close()
        conn.close()


@app.post("/cards", response_model=Flashcard)
def create_card(card_in: FlashcardIn):
    """
    Crea una nueva flashcard en FLASH.FLASHCARDS.
    """
    conn = get_connection()
    cur = conn.cursor()
    try:
        # 1) Insertar la fila
        cur.execute(
            """
            INSERT INTO FLASH.FLASHCARDS (english, spanish)
            VALUES (:english, :spanish)
            """,
            {
                "english": card_in.english,
                "spanish": card_in.spanish,
            }
        )
        conn.commit()

        # 2) Recuperar la última fila insertada (por id más alto)
        cur.execute(
            """
            SELECT id, english, spanish, created_at
            FROM FLASH.FLASHCARDS
            WHERE id = (
                SELECT MAX(id) FROM FLASH.FLASHCARDS
            )
            """
        )
        row = cur.fetchone()
        if not row:
            raise HTTPException(
                status_code=500,
                detail="No se pudo recuperar la flashcard recién creada."
            )

        return Flashcard(
            id=row[0],
            english=row[1],
            spanish=row[2],
            created_at=row[3]
        )

    except Exception as e:
        print("Error en POST /cards:", e)
        raise HTTPException(status_code=500, detail=str(e))
    finally:
        cur.close()
        conn.close()


@app.delete("/cards/{card_id}")
def delete_card(card_id: int):
    """
    Elimina una flashcard por ID en FLASH.FLASHCARDS.
    """
    conn = get_connection()
    cur = conn.cursor()
    try:
        cur.execute(
            "DELETE FROM FLASH.FLASHCARDS WHERE id = :id",
            {"id": card_id}
        )
        deleted = cur.rowcount
        conn.commit()

        if deleted == 0:
            return {
                "deleted": False,
                "message": f"No existe una flashcard con id {card_id}"
            }

        return {
            "deleted": True,
            "message": f"Flashcard {card_id} eliminada"
        }

    except Exception as e:
        print("Error en DELETE /cards/{card_id}:", e)
        raise HTTPException(status_code=500, detail=str(e))
    finally:
        cur.close()
        conn.close()

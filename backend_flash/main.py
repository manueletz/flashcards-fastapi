from fastapi import FastAPI, HTTPException
from pydantic import BaseModel
from typing import List, Optional
from datetime import datetime

app = FastAPI(title="FlashAPI", description="API de flashcards con Oracle (esquema FLASH)", version="1.0.0")
from fastapi.middleware.cors import CORSMiddleware

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],  # en producción se puede restringir a tu dominio
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

import oracledb
from db import get_connection


# =======================
#   MODELOS Pydantic
# =======================

class FlashcardIn(BaseModel):
    english: str
    spanish: str


class Flashcard(BaseModel):
    id: int
    english: str
    spanish: str
    created_at: Optional[datetime] = None


# =======================
#   INICIALIZACIÓN BD
# =======================

@app.on_event("startup")
def init_db():
    """
    Crea la tabla FLASH.FLASHCARDS si no existe.
    Se ejecuta automáticamente cuando arranca FastAPI.
    """
    conn = get_connection()
    cur = conn.cursor()
    try:
        cur.execute(
            """
            CREATE TABLE FLASH.FLASHCARDS (
                id         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                english    VARCHAR2(200) NOT NULL,
                spanish    VARCHAR2(200) NOT NULL,
                created_at TIMESTAMP DEFAULT SYSTIMESTAMP
            )
            """
        )
        conn.commit()
        print("Tabla FLASH.FLASHCARDS creada.")
    except oracledb.Error as e:
        # ORA-00955: name is already used by an existing object
        if "ORA-00955" in str(e):
            print("Tabla FLASH.FLASHCARDS ya existe, continuando...")
        else:
            print("Error creando tabla FLASH.FLASHCARDS:", e)
            raise
    finally:
        cur.close()
        conn.close()


